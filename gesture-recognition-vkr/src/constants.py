# Словарь для быстрого поиска индекса класса
CLASS_TO_IDX = {cls: idx for idx, cls in enumerate(SLOVO_CLASSES)}
IDX_TO_CLASS = {idx: cls for idx, cls in enumerate(SLOVO_CLASSES)}

def get_class_name(idx):
    """Получить название жеста по индексу"""
    return IDX_TO_CLASS.get(idx, f"unknown_{idx}")

def get_class_index(name):
    """Получить индекс жеста по названию"""
    return CLASS_TO_IDX.get(name.lower(), -1)
